let num1 = 10;
let num2 = 20;

// AND - ambas precisa ser true
if (num1 > 10 && num1 == 10) {
    console.log(`num1 é maior ou igual a 10`)
} else {
    console.log(`num1 é menor ou diferente a 10`)
}

//OR - apenas um precisa ser verdadeiro
if (num2 > 21 || num2 == 20) {
    console.log(`num2 é maior ou igual de 20`)
} else {
    console.log(`num2 é menor ou diferente a 20`)
}